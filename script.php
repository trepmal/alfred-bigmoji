/**
 * 
 * @param string $name Base name of image
 * @param int $rows Number of rows. Use 0 to indicate that there's only 1 row, but it should not be indicated in the name e.g. mosaic( 'dogrun', 0, 4 );
 * @param int $cols Number of cols. Use 0 to indicate that there's only 1 col, but it should not be indicated in the name
 * @return string
 */
function mosaic( $name, $rows=3, $cols=4 ) {

	$print_row = $print_col = true;

	if ( $cols == 0 ) {
		$print_col = false;
		$cols = 1;
	}
	if ( $rows == 0 ) {
		$print_row = false;
		$rows = 1;
	}

	$output = '';

	for ( $x=1; $x<=$rows; $x++ ) {
		for ( $y=1; $y<=$cols; $y++ ) {
			$output .= sprintf(
				":%s%s%s:",
				$name,
				( $print_row ? "-$x" : "" ),
				( $print_col ? "-$y" : "" )
			);
		}
		$output .= PHP_EOL;
	}

	return $output;
}
